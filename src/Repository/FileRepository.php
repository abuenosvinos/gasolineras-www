    /**
     * @return File[] Returns an array of File objects
     */
    public function findAllWithTotalStations()
    {
        $list = $this->createQueryBuilder('f')
            ->leftJoin('f.stations', 's')
            ->addSelect('COUNT(f.id)')
            ->addGroupBy('f.id')
            ->getQuery()
            ->getResult()
        ;

        $newList = [];
        foreach ($list as $item) {
            $newItem = $item[0];
            $newItem->setCount($item[1]);
            $newList[] = $newItem;
        }
        return $newList;
    }


